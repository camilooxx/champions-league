    private void AcceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptButtonActionPerformed
        String NamePlayer;
        String TeamToChange;
        ConsultarDatosBD CambiarDatos = new ConsultarDatosBD(); // OBJETO PARA PROCEDER A CAMBIAR DESDE BD
        NamePlayer = ListaJugadores.getSelectedValue(); // TOMAR DATO DE JUGADOR
        TeamToChange = (String) EquipoaCambiar.getSelectedItem(); // TOMAR DATO DE EQUIPO A CAMBIAR
        CambiarDatos.ChangeTeamPlayers(NamePlayer, TeamToChange);
        JOptionPane.showMessageDialog(null, "JUGADOR CAMBIADO DE EQUIPO");
        dispose();
    }//GEN-LAST:event_AcceptButtonActionPerformed

