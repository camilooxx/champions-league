    private void CheckButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckButtonActionPerformed
        // if(AQU√ç VA CONDICIONAL PARA CONSULTAR EN BASE DE DATOS) {
            active = true;
            DefaultListModel<String> Jugadores = new DefaultListModel<>();
    //       }
        if(active == true) {
            ConsultarDatosBD Listadejugadores = new ConsultarDatosBD();
            String seleccion = (String) ComboBoxTeam.getSelectedItem();
            Jugadores = Listadejugadores.ConsultarJugadores(seleccion);
            ListaJugadores.setModel(Jugadores);
            ListaJugadores.setEnabled(true);
            EquipoaCambiar.setEnabled(true);
        }
    }//GEN-LAST:event_CheckButtonActionPerformed

